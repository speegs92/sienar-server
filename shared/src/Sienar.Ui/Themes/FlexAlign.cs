namespace Sienar.Themes;

/// <summary>
/// Supported <c>align-items</c> flex values
/// </summary>
public enum FlexAlign
{
	/// <summary>
	/// Represents <c>align-items: start</c>
	/// </summary>
	Start,

	/// <summary>
	/// Represents <c>align-items: end</c>
	/// </summary>
	End,

	/// <summary>
	/// Represents <c>align-items: center</c>
	/// </summary>
	Center,

	/// <summary>
	/// Represents <c>align-items: baseline</c>
	/// </summary>
	Baseline,

	/// <summary>
	/// Represents <c>align-items: stretch</c>
	/// </summary>
	Stretch
}
