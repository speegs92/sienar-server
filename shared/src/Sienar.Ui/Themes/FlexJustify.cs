namespace Sienar.Themes;

/// <summary>
/// Supported <c>justify-content</c> flex values
/// </summary>
public enum FlexJustify
{
	/// <summary>
	/// Represents <c>justify-content: start</c>
	/// </summary>
	Start,

	/// <summary>
	/// Represents <c>justify-content: end</c>
	/// </summary>
	End,

	/// <summary>
	/// Represents <c>justify-content: center</c>
	/// </summary>
	Center,

	/// <summary>
	/// Represents <c>justify-content: space-between</c>
	/// </summary>
	Between,

	/// <summary>
	/// Represents <c>justify-content: space-around</c>
	/// </summary>
	Around,

	/// <summary>
	/// Represents <c>justify-content: space-evenly</c>
	/// </summary>
	Evenly
}
