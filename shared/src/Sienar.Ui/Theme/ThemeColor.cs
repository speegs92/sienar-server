	/// </summary>
	Success,

	/// <summary>
	/// A color indicating information
	/// </summary>
	Info,

	/// <summary>
	/// A color indicating a warning state
	/// </summary>
	Warning,

	/// <summary>
	/// A color indicating an error state
	/// </summary>
	Error,

	/// <summary>
	/// The light color
	/// </summary>
	Light,

	/// <summary>
	/// The dark color
	/// </summary>
	Dark
